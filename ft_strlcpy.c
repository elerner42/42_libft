#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dst_s)
{
	size_t	i;

	i = 0;
	if (dst_s > 0)
	{
		while (src[i] && i < (dst_s - 1))
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = 0;
	}
	while (src[i])
		i++;
	return (i);
}